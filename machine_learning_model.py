disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=['Crisis', 'Warning', 'Safe'])
disp.plot(ax=ax2, colorbar=False, cmap='Blues')
ax2.set_title('Confusion Matrix (RF Tuned Threshold)', fontweight='bold')

# Plot 3: Top 10 Feature Importance
ax3 = axes[1, 0]
top10 = feature_imp.sort_values(ascending=False).head(10)
bar_colors = ['#e74c3c'] + ['#3498db'] * 9
top10[::-1].plot(kind='barh', ax=ax3, color=bar_colors[::-1], edgecolor='black', linewidth=0.5)
ax3.set_title('Top 10 Feature Importance', fontweight='bold')
