@st.cache_resource
def load_model():
    model        = joblib.load(MODEL_PATH)
    columns      = joblib.load(COLUMNS_PATH)
    thresholds   = joblib.load(THRESHOLD_PATH)
    class_labels = joblib.load(LABELS_PATH)
    return model, columns, thresholds, class_labels

def predict(input_dict_usd, model, columns, thresholds, class_labels):
    """Prediksi dengan multi-class threshold tuning (input sudah dalam USD)."""
    df_in = pd.DataFrame([input_dict_usd])
    df_in = pd.get_dummies(df_in)
    df_in = df_in.reindex(columns=columns, fill_value=0)

    probs       = model.predict_proba(df_in)[0]
    warning_idx = class_labels.index('Warning')
    safe_idx    = class_labels.index('Safe')

    thr_warn = thresholds.get('warning', 0.33)
    thr_safe = thresholds.get('safe',    0.33)

    candidates = {}
    if probs[warning_idx] >= thr_warn:
        candidates['Warning'] = probs[warning_idx]
    if probs[safe_idx] >= thr_safe:
        candidates['Safe'] = probs[safe_idx]

    status = max(candidates, key=candidates.get) if candidates \
             else class_labels[int(np.argmax(probs))]

    prob_dict  = dict(zip(class_labels, probs))
    feat_imp   = pd.Series(model.feature_importances_, index=columns)
    input_ser  = df_in.iloc[0]
    risk_score = (feat_imp * input_ser).sort_values(ascending=False)
    top_risks  = risk_score[risk_score > 0].head(5)

    return status, prob_dict, top_risks


