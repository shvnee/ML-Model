def make_prob_chart(prob_dict):
    """Horizontal bar untuk probabilitas tiap kelas."""
    colors = {'Crisis': '#FF4757', 'Warning': '#FFA502', 'Safe': '#2ED573'}
    fig, ax = plt.subplots(figsize=(5, 2.2), facecolor='#161921')
    ax.set_facecolor('#161921')

    for i, (cls, prob) in enumerate(prob_dict.items()):
        ax.barh(cls, prob, color=colors.get(cls, '#4ECDC4'), height=0.5, edgecolor='none')
        ax.text(prob + 0.01, i, f'{prob:.1%}', va='center', fontsize=9, color='white')

    ax.set_xlim(0, 1.15)
    ax.set_xlabel('Probabilitas', color='#6B7280', fontsize=8)
    ax.tick_params(colors='#9CA3AF', labelsize=9)
    for spine in ax.spines.values():
        spine.set_visible(False)
    ax.axvline(x=0.33, color='#2A2F3E', linewidth=1, linestyle='--')
    fig.tight_layout()
    return fig


